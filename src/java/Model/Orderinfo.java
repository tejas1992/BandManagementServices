public class Orderinfo {
    
    private String band_user ;
    private String occasion ;
    private String customer_user;
    private String date;

    public String getBand_user() {
        return band_user;
    }

    public void setBand_user(String band_user) {
        this.band_user = band_user;
    }

    public String getOccasion() {
        return occasion;
    }

    public void setOccasion(String occasion) {
        this.occasion = occasion;
    }

    public String getCustomer_user() {
        return customer_user;
    }

    public void setCustomer_user(String customer_user) {
        this.customer_user = customer_user;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
    
    
    
    
    
}
